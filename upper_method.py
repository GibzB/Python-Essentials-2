# upper() method
# The upper() method makes a new string by turning all letters of the source string to upper-case.
# makes a copy of the source string, replaces all lower-case letters with their upper-case counterparts, 
#   and returns the string as the result

# Demonstrating the upper() method:
print("I know that I know nothing. Part 11.".upper())

# The upper() method has one more interesting feature:
# it can capitalize the letter preceding every single quote mark (') and double quote mark (").

# Demonstrating the upper() method's second feature:
print("I know that I'm now nothing. Part 2.".upper())