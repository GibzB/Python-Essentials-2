# title() method
# The title() method makes a new string by capitalizing every word of the source string.
# it changes every word's first letter to upper-case, turning all other ones to lower-case.

# Demonstrating the title() method:
print("I know that I know nothing. Part 1.".title())

# The title() method has one more interesting feature: 
#   it can capitalize the letter preceding every single quote mark (') and double quote mark (").
# Demonstrating the title() method's second feature:
print("I know that I'm now nothing. Part 2.".title())

