# The capitalize() method does exactly what it says – it creates a new string filled with characters taken from the source string, but it tries to modify them in the following way:

# if the first character inside the string is a letter not just the first visible character it will be converted to upper-case;
# all remaining letters from the string will be converted to lower-case.

# The capitalize() method returns a new string, so it doesn’t modify the original string. 
# It’s important to remember that the capitalize() method doesn’t modify the original string, but it returns a new string.

# The capitalize() method is a string method, so it can be used only on strings.

# Demonstrating the capitalize() method:
print('aBcD'.capitalize())
