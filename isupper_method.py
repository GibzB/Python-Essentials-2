# isupper() method
# The isupper() method returns True if all alphabets in a string are uppercase alphabets. 
#   If the string contains at least one lowercase alphabet, it returns False.
# The isupper() method is the upper-case version of islower() â€“ it concentrates on upper-case letters only.


# Example: Demonstrating the isupper() method:
print("Moooo".isupper())
print('moooo'.isupper())
print('MOOOO'.isupper())